        This application was built using a Laravel framework, following the Model-View-Controller architecture to spearate logic, data handling and presentation..<br /><br />

        The app uses Laravel's routing system to map URL's to controller methods and ORM to interact with a MySQL through a single normalised table.<br /><br />

        Migrations and seeders were implemented to allow the database structure and sample data to be recreated easily.<br /><br />

        The application supports full CRUD functionality while Blade components to provide a consistent layout.<br /><br />

        Additional features such as pagination and search improve usability and performance for users.<br /><br />

